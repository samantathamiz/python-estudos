a = 4; b=20
print (a == b)                               #compara usando == igual a
print (a != b)                               #compara usando != diferente de
print (a > b)                                #compara > maior que 
print (a < b)                                #compara < menor que
print (a >= b)                               #compara usando >= maior ou igual a
print (a <= b)                               #compara usando <= menor ou igual a
